      type: {
        default: "primary",
        type: String,
      },
      // Boder button
      btn_boder: {
        default: false,
        type: Boolean,
      },
      // boder radius button
      radius: {
        default: false,
        type: Boolean,
      },
      // trạng thái button
      disabled: {
        default: false,
        type: Boolean,
      },
      id: {
        default: null,
        type: String,
      },
      //key tab cho buttom
      tabindex: {
        default: null,
        type: [Number, String],
      },
    },
    setup() {
